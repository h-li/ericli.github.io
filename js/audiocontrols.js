


	function playDemo1() {
		var demo1 = document.getElementById("demo1");
		demo1.play();
		$("#demo-1").addClass("album-playing");
	}
	function stopDemo1() {
		var demo1 = document.getElementById("demo1");
		demo1.pause();
		demo1.load();
		$(".album").removeClass("album-playing");
	}
